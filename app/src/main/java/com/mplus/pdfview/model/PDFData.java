package com.mplus.pdfview.model;

public class PDFData {

    String name,path;
    Integer imagePdf;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPath() {
        return path;
    }

    public void setPath(String path) {
        this.path = path;
    }

    public Integer getImagePdf() {
        return imagePdf;
    }

    public void setImagePdf(Integer imagePdf) {
        this.imagePdf = imagePdf;
    }
}
